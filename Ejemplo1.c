#include <stdio.h>
#include <stdlib.h>
	
	/**
	* Pintar numeros del 1 al 10
	*/
	int main(int argc, char *argv[]) {
		
	int numeroIngresado = 10;
	
	switch(numeroIngresado){
		case 1:
			printf("Soy el numero uno");
			break;
		case 2:
			printf("Soy el numero dos");
			break;
			
		case 3:
			printf("Soy el numero tres");
			break;
						
		case 4:
			printf("Soy el numero cuatro");
			break;
			
		case 5:
			printf("Soy el numero cinco");
			break;
			
		default:	
		
		printf("No soy ningun numero");
	}
		
		
		return 0;
	}

